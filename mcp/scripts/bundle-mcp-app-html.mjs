import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

const DIST_DIR = join(__dirname, "../dist");
const OUTPUT_TS_PATH = join(
  __dirname,
  "../src/generated/McpAppsHtml.bundled.ts"
);
const APPS = [
  {
    exportName: "WHOAMI_APP_HTML",
    fileName: "src/ui/whoami.html",
  },
];

export function bundleMcpAppsHtml() {
  const output = `// This file is auto-generated by scripts/bundle-mcp-app-html.mjs
// Do not edit directly - modify the source app under src/ui instead.

${APPS.map(({ exportName, fileName }) => {
  const html = readFileSync(join(DIST_DIR, fileName), "utf-8");

  return `export const ${exportName} = ${JSON.stringify(html)};`;
}).join("\n\n")}
`;

  mkdirSync(dirname(OUTPUT_TS_PATH), { recursive: true });
  writeFileSync(OUTPUT_TS_PATH, output);
}

export function bundleMcpAppsHtmlPlugin() {
  return {
    name: "mcpjam-bundle-mcp-app-html",
    closeBundle() {
      bundleMcpAppsHtml();
    },
  };
}

const isEntrypoint = process.argv[1]
  ? import.meta.url === pathToFileURL(process.argv[1]).href
  : false;

if (isEntrypoint) {
  bundleMcpAppsHtml();
}
