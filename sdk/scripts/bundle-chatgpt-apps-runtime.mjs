import { build } from "esbuild";
import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

try {
  const result = await build({
    entryPoints: [join(__dirname, "../src/ChatGptAppsRuntime.ts")],
    bundle: true,
    write: false,
    format: "iife",
    platform: "browser",
    target: "es2020",
    minify: false,
  });

  const bundledCode = result.outputFiles[0].text;
  const serializedCode = JSON.stringify(bundledCode);

  const outputContent = `// This file is auto-generated by scripts/bundle-chatgpt-apps-runtime.mjs
// Do not edit directly - modify src/ChatGptAppsRuntime.ts instead

export const CHATGPT_APPS_RUNTIME_SCRIPT = ${serializedCode};
`;

  const outputTsPath = join(__dirname, "../src/ChatGptAppsRuntime.bundled.ts");
  writeFileSync(outputTsPath, outputContent);
  console.log("✅ Successfully bundled ChatGPT Apps runtime script");
} catch (error) {
  console.error("❌ Failed to bundle ChatGPT Apps runtime script:", error);
  process.exit(1);
}
